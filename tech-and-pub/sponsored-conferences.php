										  <a class="anchor" id="International Crosstie"></a>
<span class="subject-name">International Crosstie and Fastening System Symposium</span> – The primary objective of this symposium is to facilitate the transfer or technology and knowledge as well as to further domestic and international communication and collaboration on the design and performance challenges and solutions for crossties and fastening systems. This three day conference will have presentations, discussions, and a technical tour that focus on the state of the art in timber, concrete, steel, and composite crosstie and fastening system design, performance, research, modeling, and inspection. <br><br>
2014 - <a target="blank" href="http://railtec.illinois.edu/Crosstie/2014/crossties.php">http://railtec.illinois.edu/Crosstie/2014/crossties.php</a><br><br>
2012 - <a target="blank" href="http://railtec.illinois.edu/Crosstie/2012/crossties.php">http://railtec.illinois.edu/Crosstie/2012/crossties.php</a><br><br>




										  <a class="anchor" id="2014GLXS"></a>
<span class="subject-name">2014 Global Level Crossing Safety and Trespass Symposium</span> -GLXS 2014 is the principal international conference bringing together engineering, safety, security, human factors, practitioners, academics and researchers from the global highway, rail, law enforcement, research and regulatory communities. <br><br>
<a target="blank" href="http://railtec.illinois.edu/GLXS/overview.php">http://railtec.illinois.edu/GLXS/overview.php</a><br><br>


										  <a class="anchor" id="Railroad Environmental Conference"></a>
<span class="subject-name">Railroad Environmental Conference (RREC) </span>- <a target="blank" href="http://railtec.illinois.edu/RREC/overview.php">http://railtec.illinois.edu/RREC/overview.php</a><br><br>

This premiere annual conference is an excellent forum for exchange of information on current methods and practices employed by the Rail industry and their consultants to address current and on-going environmental issues, learn about new techniques and technologies, and generally stay in touch with the direction of the railroad industry’s environmental programs<br><br>
2014 - <a target="blank" href="http://railtec.illinois.edu/RREC/overview.php">http://railtec.illinois.edu/RREC/overview.php</a><br><br>
