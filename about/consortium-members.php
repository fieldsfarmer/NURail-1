<a class="anchor" id="Massachusetts Institute of Technology"></a>
<h4><a target="_blank" href="http://web.mit.edu/">Massachusetts Institute of Technology </a></h4>
	<h4><img src="images/logos/mit.jpg"/></h4><br>
 
Massachusetts Institute of Technology (MIT) contributes to development of solutions for regional and national transportation problems, including a long legacy of important rail contributions. Under the leadership of Professor Joseph Sussman, the high-speed rail/regional development group (http://web.mit.edu/hsr-group/index.html) is conducting path-breaking work directly related to NURail goals. Professor Sussman is the JR East Professor (endowed by the East Japan Railway Company) at MIT and through that relationship have made important contributions in HSR safety, protection from natural hazards and transportation reliability.<br><br>
	MIT’s public transportation program is highly respected, educating numerous students each year. Urban rail transportation is an area in which they make major contributions in the U.S. and abroad (e.g. Transport for London and HSR in the UK).  Through its Center for Transportation and Logistics (CTL, founded in 1973), MIT has performed path-breaking research in the rail freight sector in areas including service reliability, productivity, wheel-rail dynamics, investment and maintenance planning, and freight car utilization. In recent years, the Transportation@MIT initiative (http://transportation.mit.edu) has expanded transportation-related activities into many departments and centers at MIT.
	
