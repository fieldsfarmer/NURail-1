 <a class="anchor" id="University of Illinois at Urbana-Champaign"></a>                           
<h4><a target="_blank" href="http://illinois.edu/">University of Illinois at Urbana-Champaign</a></h4>

<h4><img src="images/logos/uiuc.png"/></h4>
 <br>

 

	University of Illinois at Urbana-Champaign (UIUC) is a leader in rail engineering research and education in North America, with a rich history of significant contributions dating back over a century. UIUC has a distinguished record of accomplishment and remains highly respected by today's railroad engineering community. Through the leadership of Professor Christopher Barkan, UIUC has substantially expanded its Rail Transportation and Engineering Center (RailTEC) through rail research and educational programs during the past 14 years. For more information see: <a target="_blank" href="http://railtec.illinois.edu">http://railtec.illinois.edu</a>.<br><br>
	UIUC is committed to and actively engaged in supporting development of other academic rail transportation programs in the U.S. UIUC’s long and distinguished leadership in railroad transportation reflects a deep institutional knowledge and understanding of the field, including expertise in traditional rail disciplines as well as emerging research areas critical to economically successful, safe, efficient and reliable passenger and freight rail.  UIUC has established itself as the leading university rail program in the United States and is one of three AAR Affiliated Labs.
	
