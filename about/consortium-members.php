<a class="anchor" id="Michigan Technological University"></a>
<h4><a target="_blank" href="http://www.mtu.edu/">Michigan Technological University</a></h4><h4><img src="images/logos/mt.jpg"/></h4><br>

 
	
Michigan Technological University’s (Michigan Tech) Rail Transportation Program (RTP) has developed under Director Pasi Lautala’s leadership into a one of the leading rail education and research programs in the U.S.  Established in 2007 within the Michigan Tech Transportation Institute (MTTI), the program places a high priority on rail transportation and engineering related education.  Continued educational development areas include a Rail/Transportation Engineering Certificate, expansion of industry-funded undergraduate Senior Design and Enterprise rail projects, as well as increasing numbers of summer internships.  NURail research collaborations with rail industry stakeholders focus on rural freight rail and multimodal transportation improvements, human factors and rail safety, infrastructure evaluation and assessment, high performance materials for railroad infrastructure preservation, and renewal and improved materials for rail industry.  The RTP provides the foundation for all rail related activities in the field and has become a permanent part of university curriculum and research at Michigan Tech.
