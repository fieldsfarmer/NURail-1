<a class="anchor" id="University of Kentucky"></a>
<h4><a target="_blank" href="http://www.uky.edu/">University of Kentucky </a></h4>
<h4><img src="images/logos/uk.jpg"/></h4> <br>


 

	The Commonwealth of Kentucky was a leader in the development of the rail system in the United States, having one of the first operative railroads in 1831. Its rail system has had a major influence on the development and sustainability of the state’s industry since the very early 1900s. Several of the nation’s major north-south heavy traffic rail lines -- CSX, NS, and CN, and an important regional, P&L – pass through Kentucky, comprising the bulk of Kentucky’s over 2,500 miles of track. This is augmented with local collector lines mainly operated by smaller rail companies, including RJ Corman RR, which provides emergency derailment response and repair services across the US.  <br><br>
	The Civil Engineering Department at the University of Kentucky (UK) was an early leader among universities in the re-establishment of an academic emphasis in railway transportation beginning in the early 1980s. The Rail-Related Research Program is a major component of UK’s railway emphasis. Numerous Research Projects have been conducted during the past thirty years and strong partnerships with Class I, regional and short lines railroads. UK’s projects are funded by a combination of railroad companies, governmental agencies, and individuals. Over sixty graduate and undergraduate students have participated in these studies.
	
	<hr class="clear-both short" /> 




<a class="anchor" id="University of Tennessee"></a>
<h4><a target="_blank" href="http://www.utk.edu/">University of Tennessee, Knoxville</a></h4><h4><img src="images/logos/utk.jpg"/></h4>
 
	The University of Tennessee, Knoxville (UTK) has been a national leader in transportation research and education for over 50 years. Students with particular interests in rail transportation are welcomed in the UT Rail Society, which is also an AREMA student chapter. <br><br>
	NURail center activities are coordinated by the UTK Center for Transportation Research (CTR). One of the oldest dedicated academic centers focusing on transport, CTR celebrated its 40th anniversary in 2012. CTR has a long tradition of excellence in rail transportation research and education, with particular strengths in economics, planning, safety, and workforce development. CTR has been a provider of education and training to the railroad industry for over 25 years. Current courses address railroad track and structures design, safety inspection, and maintenance. CTR has strong relationships with the railroad and planning offices at the Tennessee Department of Transportation, with Class 1 and shortline railroads operating in the southeast, with railroad contractors and suppliers, and with Oak Ridge National Laboratory. CTR will be the host of the 2013 Joint Rail Conference.
	
