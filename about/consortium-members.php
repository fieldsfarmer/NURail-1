<a class="anchor" id="Rose-Hulman Institute of Technology"></a>
<h4><a target="_blank" href="http://www.rose-hulman.edu/">Rose-Hulman Institute of Technology</a></h4>
<h4><img style="width:199px" src="images/logos/rhit.png"/></h4><br>

 

	Rose-Hulman Institute of Technology (Rose-Hulman) is home to 2,100 undergraduate and 100 graduate students majoring in engineering, science or mathematics.  Our mission is to provide our students with the world's best undergraduate science, engineering, and mathematics education in an environment of individual attention and support.   With a 1:13 faculty-student ratio our faculty can take a personal interest in each student's success at Rose-Hulman.  As we prepare students for their careers, our emphasis is on a hands-on education where students learn the value of teamwork as well as formulas and equations.  Rose-Hulman has been ranked No. 1 by U.S. News & World Report among institutions whose highest degree in engineering is the master's.  Among individual engineering programs, Rose-Hulman ranked No. 1 in five areas: chemical engineering, civil engineering, computer engineering, electrical engineering and mechanical engineering.<br><br>

                            
                            
                            
