                            <h1>Overview</h1>
                            The National University Rail (NURail) Center is a consortium of seven partner colleges and universities offering an unparalleled combination of strengths in railway transportation engineering research and education in North America.<br><br>
<h4><image src="images/overview/map.png" width="460px" /> </h4><br><br> 
The NURail Center is the first USDOT OST-R University Transportation Center dedicated to the advancement of North American rail transportation. The Center is headquartered  at  the  University  of  Illinois at Urbana-Champaign and includes researchers and educators who are experts and national leaders in railway infrastructure, systems  and  vehicles  from  seven  prestigious  academic institutions in the United States.<br><br>


<h4><image src="images/overview/1.jpg" width="280px" />&nbsp;&nbsp;
<image src="images/overview/2.png"  width="280px" /> </h4><br><br> 

 

<a class="anchor" id="Workforce Development"></a><h4>Workforce Development</h4>
Beyond education, the NURail Center mission includes outreach to a wide range stakeholders with the goal of developing the railway workforce of the future.  Primary and secondary students are engaged through on-campus rail  engineering  education  events,  tours,  presentations and summer programs hosted by NURail Center partner institutions.   Undergraduate students gain valuable rail industry exposure and experience through student internships and co-operative industry programs.  Experts from the railroad industry, government and academia are hosted  on  NURail  campuses  where  they give presentations on a broad range of technical and policy topics.  Their impact is further leveraged by offering these presentations to the entire rail community using web- based tools enabling broad participation.<br><br>
  
<h4><image src="images/overview/3.png" width="280px" /> &nbsp;&nbsp;
<image src="images/overview/4.png" width="280px" /> </h4><br><br> 

 

<a class="anchor" id="Rail Education"></a><h4>Rail Education</h4>
The NURail Center is leading efforts to expand and improve railway engineering and transport education in North America.  NURail Center faculty are developing new rail educational materials, courses and curricula; engaging and encouraging new faculty interest in teaching rail topics, and supporting and inspiring student interest on NURail Center campuses, and beyond.   NURail Center educators will increase the breadth and depth of rail educational materials for both undergraduate and graduate students, as well as increasing course availability through traditional, short-course and on-line delivery.<br><br>
 


<a class="anchor" id="Technology Transfer"></a><h4>Technology Transfer</h4>
NURail Center partner institutions have a long history of research contributions to the rail industry and have developed many close relationships with private and public sector rail companies and organizations. Fundamental to this success is our understanding that research results must be applicable in the rail industry thereby   facilitating   technology   transfer.      The   NURail Center is building on this model to further expand the quality and content of these relationships.   Research will be published in UTC Research Briefs and presented at railroad meetings, workshops and conferences. NURail Center partners are playing a lead role in planning and hosting the 2013 Joint Rail Conference at the University of Tennessee-Knoxville and other special-topic focused workshops relating to the center research themes.<br><br>
<h4><image src="images/overview/5.png" width="490px" /></h4> <br><br>  

<a class="anchor" id="Strategic Development Planning"></a><h4>Strategic Development Planning</h4>
Strategic development planning involves collaboration among the NURail Center partners and others to develop the long-term vision, activities, technical research tasks and timeline for specific thematic  research  topics. Planned activities include establishing working groups of leaders in various fields from the NURail Center institutions, identifying state-of-the-art practice of current research efforts and organizing symposia to coordinate ongoing technical research.<br><br>

<h4><image src="images/overview/8.png" width="280px" /> &nbsp;&nbsp;
 <image src="images/overview/9.png" width="280px" /></h4> <br><br>  
 

<a class="anchor" id="Research Themes"></a><h4>Research Themes</h4>
The NURail Center has identified six thematic research topics that will be the subject of strategic development planning and ongoing technical research<br>
• Integrated Railroad Vehicle/Track Interaction and Dynamics<br>
• Railroad Safety and Risk<br>
• Rail Network Capacity Analysis and Planning<br>
• Urban, Regional and High-Speed Passenger Rail Implementation<br>
• Multimodal Freight Transportation<br>
• Funding, Finance, Community and Economic Development<br><br>

 
 <h4><image src="images/overview/10.png" width="280px" /> &nbsp;&nbsp;
 <image src="images/overview/11.png" width="280px" /></h4> <br><br>  

 <a class="anchor" id="Technical Research"></a><h4>Technical Research</h4>
The overarching objective of the NURail Center   technical research plan is to sustain and expand U.S. freight railroad transportation safety, capacity, efficiency and reliability, while at the same time developing commuter and intercity passenger rail systems.  The divergent needs of freight and passenger trains on shared corridors present complex infrastructure, rolling stock, operational and institutional problems,  and  drives  research  towards  critical  cutting- edge  challenges  and  solutions  that  have  a  strong relevance to the rail community. NURail Center research thrust areas are focused on railroad infrastructure, rail vehicles and railway systems.<br><br>

 
 <h4><image src="images/overview/12.png" width="280px" /> &nbsp;&nbsp;
 <image src="images/overview/14.png" width="280px" /> </h4><br><br>  

<h4><a class="anchor" id="Cooperative Research and Development"></a>Cooperative Research and Development</h4>
In addition to research collaboration among NURail Center partners, faculty and students are actively involved in collaboration and cooperative research projects with other colleges and universities, railroads, railway suppliers, consulting firms and railway organizations such as the Association of American Railroads and Federal Railroad Administration.   The NURail Center collaboration and strategic development planning process strengthens these efforts by providing means for increased communication and interaction between NURail Center participants involved in the respective programs, allowing synergies to be  more  easily  identified  and  developed.     The  NURail Center is also engaged in developing international cooperative research agreements with universities, railway research institutes and railroads in Europe and Asia, allowing the consortium to draw upon the global body of railroad  knowledge  and  facilitate  its  use  by  North American railroads.


                            
                            
