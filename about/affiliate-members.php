                            <h1>Affiliate Members</h1>
                            A growing demand for rail transportation education and research requires a growing academic capacity in the United States. NURail members are well positioned to facilitate this change. As part of our efforts to broaden the NURail Center’s impact, we have created a NURail Affiliates program involving faculty from other colleges and universities. Benefits of this program include: <br><br>
                            <ul>
                              <li>Participation in NURail’s annual meeting, workshops, seminars and conferences</li>
                              <li>Visits to campuses</li>
                              <li>Regular in-person meetings, teleconferences and/or webinars</li>
                              <li>No artificial membership limits</li>
                              <li>Recognition as university with railway related activities</li>
                              <li>Resource sharing opportunities</li>
                              <li>Research and education collaborative opportunities</li>
                            </ul>

If you are interested in more information about our affiliate program, contact Dr. Pasi Lautala <a href="mailto:ptlautal@mtu.edu">ptlautal@mtu.edu</a>, Associate Director of Education for NURail, or Dr. Dimitris Rizos (<a href="mailto:rizos@cec.sc.edu">rizos@cec.sc.edu</a>), Chair of the Affiliate University Program.<br>
Or, if you wish to apply to be an affiliate member, please complete this <a target="blank" href="https://docs.google.com/forms/d/1vnOUC0p17iOPOc8okaO1xjXbbGfhm7WXWw9Mku298CA/viewform?usp=send_form">application form</a>.<br><br>
