URBANA, IL (June 23, 2014) - Professors from around the country attended the Railway Engineering Education Symposium (REES) on June 23-25, 2014 in Overland Park, Kansas. NuRail helped sponsor the event. The objective of the symposium was to further inform the professors of the workforce crisis and give them materials to educate students about railway infrastructure, performance, engineering, and design at their respective universities. It also included trips to BNSF Railway’s Freight Yard and the Kansas City Intermodal Facility. Click <a target="blank" href="https://www.arema.org/education/rees/2014/highlights.aspx">here</a> to see additional pictures from REES. <br><br>
 
