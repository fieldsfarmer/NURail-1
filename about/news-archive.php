URBANA, IL (November 20, 2013) -  Martin B. Hamper, a PhD student in the Dynamic Simulation Laboratory at the University of Illinois at Chicago, is the 2013 NURail Student of the Year. Martin’s research is primarily focused in the area of multibody contact problems with applications to vehicle/track interaction in railroad vehicle dynamic simulations. He will receive a $1,000.00 honorarium plus the cost of attendance to the 93rd TRB Annual Meeting, two free registrations to the CUTC Banquet, and a certificate from USDOT. Congratulations Martin! <br><br><br>
URBANA, IL (September 26, 2013) - The U.S. Department of Transportation (USDOT) has awarded a grant of $1.4 million to the National University Rail (NURail) Center, a multi-university rail transportation and engineering research center led by the University of Illinois at Urbana-Champaign. The NURail Center was established in 2012 with funding from the USDOT as the nation's first rail-specific UTC. This new grant emphasizes work toward the USDOT's strategic goal of economic competitiveness and will address rail issues related to infrastructure and vehicle engineering, capacity planning and optimization, operational safety and risk mitigation, systems integration and finance and policy. Click <a target="blank" href="http://cee.illinois.edu/NURail_grant_sept2013">here</a> to read more. Click <a target="blank" href="http://www.rtands.com/index.php/safety-training/nurail-center-among-usdot-grant-winners.html">here</a> to read the RT&S article. <br><br>
