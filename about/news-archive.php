URBANA, IL (June 18, 2014) – Professor Christopher P.L. Barkan, Executive Director of RailTEC and Director, National University Rail (NURail) Center, testified in front of the U.S. House Committee on Science Space and Technology’s Subcommittee on Research and Technology.  He and several other witnesses testified to the Subcommittee about the “Future of Surface Transportation".  As part of his testimony Dr. Barkan made several references to the ongoing activities of the NURail UTC.  Click <a target="blank" href="http://science.house.gov/hearing/subcommittee-research-and-technology-hearing-future-surface-transportation">here</a> to read his testimony or view an archive of the webcast. <br><br>
