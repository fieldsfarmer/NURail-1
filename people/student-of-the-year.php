                            <h1>NURail Student of the Year Award</h1>
                             The NURail Center designates an “outstanding graduate student” through a program jointly sponsored by the U.S. Department of Transportation and the Council of University Transportation Centers (CUTC).<br><br>
Eligibility is limited to graduate students within the NURail Center consortium who: are US citizens or permanent residents; engaged in the study of rail transport; and have previously received financial support from NURail.
The student awardee is: (i) honored with others from around the country at a special ceremony held in Washington D.C. as part of a TRB Annual Meeting; (ii) receive $1,000, along with the registration, travel and lodging costs to attend the Annual Meeting; (iii) receives two complementary registrations to the CUTC Banquet; and (iv) awarded a certificate from USDOT.
See the award Winners below.<br><br>


<a class="anchor" id="2014"></a>
<span class="subject-name">2014 – NURail Student of the Year Award </span><br><br>


<img class="leader-photo" src="images/photos/award/james.jpg"/> 
University of Illinois at Chicago (UIC) student James O’Shea is the 2014 NURail Center Student of the Year. James is currently pursuing his Ph.D. in Mechanical Engineering. His research activities focus on railroad vehicle stability and the analysis of vehicle derailment criteria. 
<br><br>
This area of research is becoming increasingly significant with the development of high speed rail and James hopes to investigate the consequences of using inadequate derailment criteria in order to promote safety in the rail community.
<br><br>
He was nominated by Dr. Ahmed Shabana, Richard and Load Hill Professor of Engineering at UIC. Dr. Shabana stated, “James remains accountable for his work, continuing to follow up on questions asked by colleagues about his work that was written almost a year before. James often has the opportunity to interact with industry personnel or academic colleagues from other universities, during which he displays a neat appearance and polite demeanor. James’s ability to perform his work efficiently and thoroughly only further demonstrates his level of professionalism.”
<br><br>
James' leadership is demonstrated by his involvement in the NURail Student Leadership Council (SLC), for which he attended both the SLC Kickoff Meeting and the SLC meeting held at the 2013 NURail Annual Meeting at UIUC. When the time came to begin selecting representatives from the participating universities, James quickly expressed interest and was therefore nominated for the position. He has published one journal article (three more are in review or preparation) and two conference papers, given three conference presentations, and served as a reviewer of technical papers submitted to ASME conferences as well as IMechE and ASME transactions. As a mentor to two visiting international students, James collaborated with them on papers in the field of railroad vehicle stability.
<br><br>
As the recipient of this award, James received an honorarium from NURail in recognition of his outstanding technical research, record of publications and presentation, academic performance, and professional leadership plus the cost of attendance to the 94th TRB Annual Meeting, two free registrations to the CUTC Banquet, and a certificate from US DOT. 

<br><br>


<a class="anchor" id="2013"></a>
<span class="subject-name">2013 – NURail Center award </span><br><br>

 
<img class="leader-photo" src="images/photos/award/martin.jpg"/> 
Martin B. Hamper, a PhD student in the Dynamic Simulation Laboratory at the University of Illinois at Chicago, was the 2013 NURail Student of the Year. Martin’s research is primarily focused in the area of multibody contact problems with applications to vehicle/track interaction in railroad vehicle dynamic simulations. He has worked on two main projects in this area. In the first, a method in which rail flexibility may be modeled without the use of a finite element program was developed. <br><br>
 

The second project is focused on the development of a representation for rails with variable cross-sectional geometry, such as switch points and frogs. The goal of this project is to allow for simple and accurate development of contact surface models for vehicle/track simulations based on input from either virtual prototyping or measured data sources. <br><br>

Professor Ahmed Shabana, University of Illinois at Chicago, stated: “Martin has received exemplary marks in each course taken as a graduate student. Throughout his studies he has demonstrated excellent abilities in programming, analytical, and writing assignments. Martin has demonstrated excellent qualities through his teaching assistantship for the Mechanisms and Dynamics of Machinery course by tutoring the students who voluntarily attend his office hours, frequently exceeding the allotted time in order to ensure that the students gain adequate understanding of the course materials.”<br><br>

Martin has published 3 peer-reviewed journal articles and 4 conference papers, is a member of the ASME student chapter and the Pi Tau Sigma mechanical engineering honor society, and a reviewer for the Proceedings of the Institution of Mechanical Engineers. He received a $1,000.00 honorarium plus the cost of attendance to the 93rd TRB Annual Meeting, two free registrations to the CUTC Banquet, and a certificate from US DOT.  Martin will defend his thesis in May 2014and will graduate in August 2014. He has a job at Toyota Technical Center in Ann Arbor, MI as a CAE engineer. 

