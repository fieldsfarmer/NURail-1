David B. Clarke is Executive Director of the UTK Center for Transportation Research and Research Associate Professor in the Department of Civil and Environmental Engineering. Dr. Clarke has served as a university faculty member, consulting engineer, research manager, and researcher. His transportation experience includes a variety of projects in the areas of planning, design, system operations, and research. He is active in national efforts to promote railway research and education, and teaches classes and short courses on rail subjects. He is active in AREMA, ASCE and TRB. He is Chairman of the ASCE Rail Transportation Committee, is an Emeritus Member and former Chair of the TRB Freight Rail Committee and is Chairman of the 2013 Joint Rail Conference, the principal North American rail research conference.<br><br>

