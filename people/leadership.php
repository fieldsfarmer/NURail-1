Stephen E. Schlickman is Executive Director of the Urban Transportation Center at UIC. He has served as adjunct faculty in the Urban Planning and Policy Department since 1999, teaching graduate courses in the funding and finance of transportation projects. He served as Executive Director of the Regional Transportation Authority from 2005-2010 where he was responsible for overseeing, planning and coordinating the region’s transit operating agencies. He managed his own consulting practice providing financial, project policy and legislative advice to clients involved in all modes of surface transport, and managing the team designing a new transit system for downtown Chicago. He is a member of the Illinois Public Transportation Association, serving as its president in 1989; on the legislation committee for the American Public Transit Association (APTA); on the board for the Chicago Metropolitan Agency for Planning; and founding chairman of the New Starts Working Group, a national coalition of major transit projects.<br><br>

