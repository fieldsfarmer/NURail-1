Christopher P. L. Barkan is Professor, Executive Director of the Rail Transportation and Engineering Center (RailTEC), and George Krambles Faculty Fellow in the Department of Civil and Environmental Engineering at UIUC where he has been on the faculty for over 14 years. As Director of RailTEC he is responsible for railroad engineering research and academic activities at UIUC and supports research on a wide range of topics to improve rail safety, reliability and efficiency. Prior to joining UIUC he directed the railroad industry’s risk engineering research in the AAR’s Research & Test and Safety & Operations Departments. He has been actively involved in positions of leadership at the Transportation Research Board (TRB) and the American Society of Civil Engineers (ASCE). Dr. Barkan is the author or editor of more than 100 railroad-engineering papers, reports, chapters or books on a range of topics. He has been an invited or keynote speaker at conferences, universities and corporations around the globe.<br><br>

