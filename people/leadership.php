Pasi T. Lautala is Assistant Professor and Director of the Rail Transportation Program at Michigan Tech. He is the Associate Director for Education of NURail. Dr. Lautala is a national leader in re-establishing rail transportation education in North American universities, and has developed several courses at Michigan Technological University’s on railroad topics. He was co-PI on an international cooperative project studying United States and European rail educational activities (TUNRail) and is actively involved in international rail educational initiatives. Prior to his academic appointment, he was employed in railroad planning, design, and operations in the United States and Finland, and is a member of several professional associations including AREMA, ASCE, ASEE and TRB.<br><br>


