Ahmed A. Shabana is a University Distinguished Professor and the Richard and Loan Hill Professor of Engineering at UIC. He is an internationally recognized leader in the field of computational mechanics and nonlinear multibody dynamics, including rail applications. Dr. Shabana’s railroad research developing algorithms for railroad vehicle and track dynamics has been funded by the FRA since 1999 and he has developed computer programs that are licensed to industry and federal agencies. He is the author of Railroad Vehicle Dynamics: A Computational Approach and four other books. He has published 192 refereed journal articles (27 in rail) and 238 conference papers and technical reports (57 in rail), supervised 38 Ph.D. students (8 in rail) and 44 M.S. students (9 in rail). He is the Founding Chair of the American Society of Mechanical Engineers (ASME) Technical Committee on Multibody 
Systems and Nonlinear Dynamics and is Editor-in-Chief of the ASME Journal of Computational and Nonlinear Dynamics; he also serves as associate editor or editorial board member for 11 other journals.<br><br>

