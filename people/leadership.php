James L. McKinney is Emeritus Professor of Civil Engineering at RHIT. Prior to his retirement in 2012, McKinney was the R. C. Hutchins Distinguished Professor of Civil Engineering. He also served as Chairman of the Civil Engineering Department from 1982-2000 and 2005-2006. He is currently developing an interdisciplinary Railroad Engineering course as part of the NURail Center. He is also the founding faculty advisor for the Rose-Hulman AREMA Student Chapter. His teaching areas included surveying, construction engineering, cost engineering, engineering economics, civil engineering materials, and transportation engineering. He is a Life Member of the American Society of Engineers, serving as the Indiana Section President 1987-1988. He is the Hot Mix Asphalt Quality Assurance Program Coordinator for the Indiana DOT and the Asphalt Pavement Association of Indiana.<br><br>

