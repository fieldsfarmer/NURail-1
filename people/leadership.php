Joseph M. Sussman is the JR East (East Japan Railway Company) Professor in the Department of Civil and Environmental Engineering and the Engineering Systems Division at MIT. He has served as an MIT faculty member for 45 years. He is the author of Introduction to Transportation Systems, a graduate text in use at a number of universities, and Perspectives on Intelligent Transportation Systems. He received the Roy W. Crum Distinguished Service Award from TRB, its highest honor, “for significant contributions to research” in 2001, and the Council of University Transportation Center (CUTC) Award for Distinguished Contribution to University Transportation Education and Research in 2003. In 2002 Intelligent Transportation System (ITS) Massachusetts named its annual “Joseph M. Sussman Leadership Award” in his honor. Dr. Sussman’s research in railroads has had a major impact on the industry in the United States and abroad with several prize-winning papers.<br><br>

