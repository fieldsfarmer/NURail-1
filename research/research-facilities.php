							 <h4> University of Illinois at Urbana-Champaign</h4>
							 <span class="subject-name">Newmark Civil Engineering Laboratory</span><br>
Newmark Civil Engineering Laboratory on the UIUC campus is the home of RailTEC faculty and student offices.  The RailTEC graduate student lab offers a variety of computer-based rail network simulation, optimization, statistical data analysis and structural finite element modelling software.
<h4><img width="45%" class="clear-both" src="images/buildings/newmark.jpg"/></h4><br><br>


<span class="subject-name">RailTEC Research and Innovation Laboratory (RAIL)</span><br>
Located just off-campus, RAIL offers full-scale rail track structure and components testing capabilities that are unmatched in North America.  The following research equipment is located at the RAIL facility:<br><br>
<ol>
  <li>Pulsating Loading Testing Machine (PLTM)</li>
    <ul>
      <li>Owned by Amsted RPS, maintained and managed by UIUC since 2010</li>
      <li>Currently used to perform AREMA Test 6</li>
      <li>One 50,000 lb (222kN) vertical actuator one 35,000 lb (156kN) lateral actuator</li>
      <li>Ability to simulate various L/V force ratios (e.g. track conditions)</li>
      <li>Adaptability for various support and service conditions (ballast box and environmental chamber)</li>
    </ul>
  <li>Track Loading System (TLS)</li>
    <ul>
      <li>Construction completed in 2014</li>
      <li>Currently used to understand the mechanics of the concrete crosstie and elastic fastening system</li>
      <li>One 50,000 lbs (222kN) vertical actuator one 35,000 lbs (156kN) lateral actuator</li>
      <li>Full-depth track substructure for representative support conditions</li>
      <li>Loading via wheelset for representative loading conditions</li>
      <li>Ability to change the L/V and magnitude of loads applied</li>
      <li>Ability to answer infrastructure and substructure research questions</li>
    </ul>
  <li>Static Tie Tester (STT)</li>
    <ul>
      <li>Owned by RailTEC since 2011</li>
      <li>Currently used to understand the moment capacity of various types of crossties</li>
      <li>Two 95,000 lb (422kN) hydraulic cylinders</li>
      <li>Ability to simulate various support conditions (e.g. track conditions)</li>
      <li>Ability to study the rail seat and pad compression</li>
    </ul>
  <li>Control Platform</li>
    <ul>
      <li>The PLTM and the TLS are operated from the control platform</li>      
    </ul>
  <li>Instrumented Wheel Set (IWS)</li>
    <ul>
      <li>On loan from TTX</li>
      <li>Ability to accurately characterize the wheel rail input forces</li>
    </ul>
  <li>Crosstie Storage</li>
    <ul>
      <li>Currently used to store various crosstie designs</li>
    </ul>
  <li>Rapid Component Deterioration System (RCDS), Future</li>
    <ul>
      <li>Ability to simulate various support conditions (e.g. track conditions)</li>
      <li>Ability to refine instrumentation techniques</li>
      <li>Ability to study the deterioration rates of various track components</li>
    </ul>
</ol>



