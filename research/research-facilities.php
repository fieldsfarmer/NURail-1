<span class="subject-name">Advanced Transportation Research and Engineering Laboratory (ATREL)</span><br>
Located in nearby Rantoul, IL, ATREL is the site of research on railway ballast and related geotechnical phenomena.  Specific equipment includes:<br><br>

1. Ballast Test Bed Setup with a 110 kip actuator. The actuator is operated under a precision servo-valve control in a closed-loop, servo-hydraulic system powered by a water-cooled high-pressure pump and regulated by a service manifold. This setup has recently been used to investigate ballast degradation and permanent deformation accumulation under dynamic loading of wood and steel ties on track.<br><br>
2. Triaxial Ballast Tester equipped with a 22 kip servo-hydraulic actuator. Tests can be performed in a “load controlled” or “displacement controlled” mode, to accommodate repeated loads (dynamic) or monotonic strain-controlled shear strength testing of ballast samples.<br>
 <h4><img width="45%" class="clear-both" src="images/buildings/3.jpg"/></h4><br>
 
<span class="subject-name">Traffic Operations Lab (TOL)</span><br>
TOL has about 7,400 sq. ft. of available building space nearly half of which is comprised of offices, classrooms, and testing areas, and the rest is a 17-foot high bay area. The laboratory was established in 1998 to train Illinois Department of Transportation personnel and contractors in the integration and working relationship of the railroad and highway signal systems and has been continually updated since then. The lab has a variety of testing equipment suited for the evaluation of traffic signal components and fiber optic communications. An extensive collection of traffic signal control hardware, including controllers, detector units, and conflict monitors, provide a valuable resource for hands-on instruction and research.
 <h4><img width="35%" class="clear-both" src="images/buildings/TOL-1.jpg"/> <img width="35%" class="clear-both" src="images/buildings/TOL-2.jpg"/></h4> <br>

<span class="subject-name">Wood Engineering Laboratory</span><br>
Located at 1305 South Goodwin Ave., Urbana IL, the Wood Engineering Lab belongs to the College of Agricultural, Consumer and Environmental Sciences.  Currently not in service, this facility was formerly used to perform a rapid aging process in wooden ties, treated or untreated with preservatives, such as creosote or borate.<br>

 <h4><img width="70%" class="clear-both" src="images/buildings/woodlab.jpg"/></h4><br>


<span class="subject-name">Field Sites</span><br>
RailTEC actively participates in field research at the Transportation Technology Center in Pueblo, Colorado and various "super sites" on key North American railway mainlines where instrumentation is installed to collect data on track performance under actual service conditions. <br>




