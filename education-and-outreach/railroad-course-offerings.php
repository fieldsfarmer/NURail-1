							<a class="anchor" id="University of Illinois at Urbana-Champaign"></a>
                            <h4> University of Illinois at Urbana-Champaign</h4>

•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee408.php">CEE 408</a> - Railway Transportation Engineering<br>
<p class="tab">This course includes an introduction to highway-rail grade crossings and railroad track system design, components, roadbeds, and maintenance.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee409.php">CEE 409</a> - Railroad Track Engineering<br>
<p class="tab">This course provides in-depth understanding of railroad track engineering concepts including track component and system design, construction, evaluation, maintenance, load distribution, and wheel/rail interaction. 
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee410.php">CEE 410</a> - Railway Signaling and Control<br>
<p class="tab">The course begins with review of train resistance, power, speed and acceleration calculation and then covers stringline diagrams, train scheduling and train stopping distance calculation. Concepts of temporal and spatial separation of trains to ensure safe and efficient use of equipment and infrastructure are introduced.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee411.php">CEE 411</a> - Railroad Project Design and Construction<br>
<p class="tab">This course provides a comprehensive, integrated understanding of the process of planning and designing a railroad-engineering project from concept through to operation.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee598hsr.php">CEE 498 HSR</a> - High-Speed Rail Engineering<br>
<p class="tab">Although the basic elements of the technology resemble conventional rail transport, there are numerous engineering design differences, including the subgrade, track system, motive power, rolling stock, traffic control, power distribution system, traffic control and station design. Recent plans for development of HSR in North America mean that engineers must understand
these engineering elements, as well as the planning, economics, construction, operation, maintenance, management and other principles of HSR systems, in order to successfully develop them.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee498hrp.php">CEE 498 HRP</a> - High-Speed Rail Planning<br>
<p class="tab">This course provides an introduction to planning of an HSR passenger transport system. Students learn about steps involved in HSR planning and methodologies for station and route selection, capital cost estimation, operation and maintenance costs estimation, ridership forecasting, economic analysis, project implementation and financing. 
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee498hrm.php">CEE 498 HRM</a> - High-Speed Rail Construction Management<br>
<p class="tab">This course will review the characteristics of a HSR construction project, and introduce the phases and processes of constructing a HSR system.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee498tsr.php">CEE 498 TSR</a> - Transportation Safety and Risk<br>
<p class="tab">This course provides an introduction to transportation risk concepts, risk management framework and risk assessment methodologies to address safety and security of freight and hazardous materials transport by railways, roads, waterways and pipelines
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee598ate.php">CEE 598 ATE</a> - Advanced Track Engineering<br>
<p class="tab">This course will examine the theory of track design; the practical application of track engineering principles; and technology advances in track evaluation, modeling and maintenance.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee598rtd.php">CEE 598 RTD</a> - Railway Terminal Design and Operations<br>
<p class="tab">In this course, students learn details of the design, operations planning, management and optimization of the terminal facilities required for the railway network to function as an efficient freight transportation system.
</p><br>
•	<a target="blank" href="http://railtec.illinois.edu/CEE/cee598src.php">CEE 598 SRC</a> - Shared Rail Corridor Engineering<br>
<p class="tab">Introduction to the engineering, operational and institutional requirements and challenges of operating higher-speed passenger trains on infrastructure also used for heavy axle- load freight trains.
</p><br>
• <a target="blank" href="http://railtec.illinois.edu/CEE/jointCoursesKTH.php">Joint Courses</a> with KTH Royal Institute of Technology, Sweden<br><br>

•	CEE 498 RVT - Rail Vehicle Technology<br>
<p class="tab">This course describes the components of rail vehicles as well as their design to meet various demands, including railcar suspension and carbody tilting systems.
</p><br>
•	CEE 598 RVD - Rail Vehicle Dynamics<br>
<p class="tab">This course provides an understanding of the dynamic interaction between vehicle and track by describing superlevation and curve negotiation, vibration and ride comfort, hunting and conicity, wheel-rail contact, wheel and rail wear, and derailment safety.
</p><br>
•	CEE 498 ET - Electric Traction<br>
<p class="tab">An introduction to electric railway traction including traction mechanics, rail systems, electrical drives, and power supplies.
</p><br>

• RailTEC <a target="blank" href="http://railtec.illinois.edu/CEE/complementarycourses.php">Affiliated Courses</a> 
<p class="tab">RailTEC affiliated courses are taught by other transportation and civil engineering faculty actively engaged in railroad research projects (RailTEC Affiliated Faculty). While not primarily focused on rail, these classes offer significant railroad content.
</p><br>
•	CEE 509 - Transportation Soils<br>
<p class="tab">This covers occurrence and properties of surficial soils, soil classification systems, soil variability; subgrade evaluation procedures, repeated loading behavior of soils; soil compaction and field control; soil moisture, soil temperature, and frost action; soil trafficability and subgrade stability for transportation facility engineering.
</p><br>
•	CDD 4908 PT - Public Transportation<br> 
<p class="tab">This course provides an in-depth overview of the fundamental principles of efficient operations, management, and planning of public transportation systems.
</p><br>
