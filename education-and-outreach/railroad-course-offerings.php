<h4> University of Illinois at Chicago</h4>
• <a target="blank" href="https://www.uic.edu/ucat/courses/CME.html">CME 404</a> - Railroad Track Engineering<br>
<p class="tab">This course will teach the students the design and analysis of railroad tracks, construction, evaluation, and maintenance of railroad tracks, load distribution, track subgrade, and it will familiarize the students with the AREMA code specifications.
</p>
