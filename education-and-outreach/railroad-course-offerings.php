<a class="anchor" id="University of Kentucky"></a>
<h4> University of Kentucky</h4>
• <a target="blank" href="http://www.engr.uky.edu/~jrose/index433.html">CE 433-001</a> - Railway Freight & Passenger Operation and Transportation<br>
<p class="tab">The course emphasizes the transportation engineering aspects of railway operations and efficient movement of freight and passengers. It provides students with instruction in the critical concepts and planning of railway operations.
</p><br>

• <a target="blank" href="http://www.engr.uky.edu/~jrose/ce533_html/">CE 533-001</a> - Railroad Facilities Design and Analysis<br>

<p class="tab">This course provides civil engineering graduate and undergraduate students a transportation technical elective course emphasizing the civil engineering aspects of Railroad Engineering. 
</p><br>

• CE599 - Urban Mass Transit<br>
<p class="tab">This course provides overview on the urban mass transit system, from its system and technology. As an technical elective course, the emphases include transit operation elements, service planning and scheduling, demand analysis, capacity, line and network design, special operations, and transit alternative analysis.
</p>
<hr class="clear-both short" />

<a class="anchor" id="University of Tennessee"></a>
<h4> University of Tennesee Knoxville</h4>
• <a target="blank" href="http://catalog.utk.edu/preview_course_nopop.php?catoid=17&coid=100018">CE 547</a> - Design of Railway Transportation Systems<br>
<p class="tab">This course covers the basics of railway transportation, track deign, geometric design of railroad lines and terminals, construction and maintenance, train performance, signaling and communication, and capacity analysis and operation.
</p>
