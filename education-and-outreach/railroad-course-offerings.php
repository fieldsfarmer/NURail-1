                            <h1> Railroad Course Offerings</h1>
                            <p>
                              &nbsp &nbsp The changing U.S. transportation environment has led to 
                              a renaissance in rail transport in the context of high 
                              energy prices, adverse environmental impacts of the present 
                              system, expensive infrastructure investments, highway and 
                              airport congestion, demand for urban rail systems, the vision 
                              for higher speed passenger rail, and the need for a truly 
                              integrated national transportation system. Consequently, 
                              there is a corresponding need to revitalize railroad 
                              transportation and engineering educational curricula, 
                              programs, and activities in the U.S. 
                            </p>
                            <p>
                              &nbsp &nbsp Fortunately, over the past 15 years members of the NURail Center have recognized this need and begun to address it. UKY began teaching two rail classes in the 1990s. MIT continued to include strong rail content in its transportation systems and public transport classes. UIUC, which previously had been teaching only one rail course, hired a new faculty member in 1998 to rebuild their rail curriculum, increasing the number of classes in rail and public transport from one to seven. In the early 2000s, MTU developed an innovative international rail studies program and then rail classes. Most recently, UTK and UIC launched new rail courses, and RHIT incorporated new rail content into its existing courses.
                            </p>
                            <p>
                              &nbsp &nbsp As encouraging as these developments are, they are not nearly enough to meet the nation’s pressing need for a new generation of railroad transportation and engineering professionals. Addressing this challenge is a major thrust of the NURail Center. The table below shows each partner university and the rail courses they currently offer. 
                            </p>
