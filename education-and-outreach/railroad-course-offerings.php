<a class="anchor" id="Michigan Technological University"></a>
<h4> Michigan Technological University</h4>
• <a target="blank" href="http://www.rail.mtu.edu/education.htm">CE 4490</a> - Rail Transportation Seminar<br>
<p class="tab">The course covers presentations and discussion of current literature, research and development related to rail transportation. It will mix industry and academic presentations with reading assignments and topics will be selected jointly between the course members during the first week of semester.
</p><br>

• <a target="blank" href="http://www.rail.mtu.edu/education.htm">CE 5409</a> - Railroad Track Engineer and Design<br>

<p class="tab">The course introduces students to the design and development of construction documents 

for railroad projects, especially to the construction of a new, or modification of an existing track.
</p><br>

• <a target="blank" href="http://www.rail.mtu.edu/education.htm">CE 5408</a> - Public Transit<br>

<p class="tab">This course give student an introduction to public transit, user characteristics, 

management, transit modes, data collection and surveys, planning, operations, scheduling, transit 

finances, and future trends.
</p><br>

• <a target="blank" href="http://www.rail.mtu.edu/education.htm">CE 4404</a> - Railroad Engineering<br>

<p class="tab">This course gives students an overview of basic elements and roles of rail transportation, 

history, organizations and economics, safety, intercity and urban passenger rail, freight 

operations, track-train dynamics, signals and communications, motive power and equipment, 

track components, construction and maintenance.
</p>
