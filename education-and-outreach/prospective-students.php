                            <h1> Prospective Students</h1>
                             
							 
	<img style="height:220px; margin:10px; float:right;"  src="images/2013 Summer Rail 2.JPG"/>
Add your story to the legacy of railroad engineering!  Alumni of railroad engineering programs have a long tradition of occupying senior engineering positions at major railroads, consulting firms, government agencies and research organizations.  Our current generation of graduates are rapidly progressing to positions of increasing responsibility and have been identified as rising stars by railway industry publications.   NURail Center universities are constantly on the lookout for motivated students become the railroad engineers of the future.<br><br>

All NURail Center universities have course offerings in rail transportation. In addition to course offerings, there are a variety of research opportunities available for both undergraduate and graduate students in the laboratory, field and office environment. Active student participation in research facilitates experiential learning and the development of skills directly applicable to successful future careers in the rail industry.<br><br>
<h4><img style="height:220px; margin:10px;   clear:both;"  src="images/DSC_9859.JPG"/><img style="height:220px; margin:10px;   clear:both;"  src="images/Rose-Hulman students 4-12-14 28.jpg"/></h4><br>
<span class="subject-name">Laboratory and Field Research</span><br>
In the laboratory, students work with a variety of loading equipment and instrumentation to conduct tests on new track components under controlled conditions.  Students may also actively participate in field research where instrumentation is installed to collect data on track performance under actual service conditions.  Once field and lab data is collected, teams of students process the data and use statistical software to analyze and interpret the results.  Other students use these results to construct, calibrate and validate finite element analysis models of the track structure system and develop new mechanistic approaches to track design.<br><br>

<span class="subject-name">Simulation and Data Analysis</span><br>
Students may also investigate broader system and network-level engineering challenges that can only be addressed through simulation and data analysis.  Students use train operations and dispatch simulation software to investigate the ability of the rail network to support increased freight rail demand and more frequent higher-speed passenger trains.  Other students conduct statistical analysis and modeling activities on train accident and derailment data to support railway safety and risk research that ensures the safe and efficient movement of good and people by rail.<br><br>
<img style="height:220px; margin:10px;   float:right;"  src="images/Senior Design 4-30-13 17.jpg"/>
<span class="subject-name">Undergraduate Research</span><br>
NURail Center Universities actively support undergraduate students participating in rail research activities.  Since most rail courses require junior or senior standing, undergraduate research is an excellent way for students who are interested in rail transportation to become involved earlier in college.  It also affords an opportunity for students to learn more about the rail industry before being faced with important decisions on technical electives and future career paths.<br><br>

To find a program that best fits you, please look at the websites for each of the NURail Center members.</span><br><br>
