              <h1>Student Groups</h1>Below are links of the AREMA student groups associated with the NURail Center partner
              universities. The goal of each of these groups is to bring students interested in railroading together to facilitate
              and advance their academic curiosity in railway engineering.
              <br />
              <br />
              <a class="anchor" id="Michigan Technological University"></a>
              <h4>Michigan Technological University</h4>The Railroad Engineering and Activities Club (REAC) formed in 2005 as an
              official student organization on campus. In 2006, REAC became the first 
              <a target="blank" href="%20http://www.arema.org/">American Engineering and Maintanence-of-Way Association (AREMA)</a>
              student chapter. REAC is highly active, hosting monthly meetings, social events, field trips, and volunteer events.
              For more information, please visit the 
              <a target="blank" href="%20http://reac-students.blogspot.com/">REAC website</a>.
              <br />
              <br />
              <a class="anchor" id="University of Illinois at Urbana-Champaign"></a>
              <h4>University of Illinois at Urbana-Champaign</h4>The AREMA Student Chapter at the University of Illinois at
              Urbana-Champaign (UIUC) formed in the fall of 2008. The goal of this chapter is to expose students to railroad
              engineering education and the railway industry in order to foster the development of leaders in railroad engineering
              in North America and throughout the world. For more information, please visit their 
              <a target="blank" href="%20https://sites.google.com/site/aremauiuc/">website</a>.
              <br />
              <br />
              <a class="anchor" id="University of Kentucky"></a>
              <h4>University of Kentucky</h4>RailCats is the UK Student Chapter of AREMA. They are one of only a handful of AREMA
              student chapters across the country and was founded in October of 2011. RailCats is an educational, outreach, and
              social organization for UK students interested in railroad engineering and the railway industry. For more
              information, please visit the 
              <a target="blank" href="%20http://railcats.engineering.uky.edu/">RailCats website</a>.
              <br />
              <br />
              <a class="anchor" id="Rose-Hulman Institute of Technology"></a>
              <h4>Rose-Hulman Institute of Technology</h4>The Rose-Hulman AREMA Student Chapter was established in December of
              2012. This group acquaints students with railway engineering topics and trends through speakers and field visits. For
              more information, please visit their 
              <a target="blank" href="%20http://www.rose-hulman.edu/civil.aspx">website.</a>
              <br />
              <br />
              <a class="anchor" id="University of Tennessee"></a>
              <h4>University of Tennessee</h4>The Rail Society at the University of Tennessee in Knoxville is a student group that
              highlights trains, railroads, and careers in the rail industry. It was established in 2009 and is an AREMA Student
              Chapter. For more information, please visit their 
              <a target="blank"
              href="%20https://www.facebook.com/pages/Rail%C3%A2%C2%80%C2%90Society%C3%A2%C2%80%C2%90at%C3%A2%C2%80%C2%90UT/199740493490871">
              website</a>.
