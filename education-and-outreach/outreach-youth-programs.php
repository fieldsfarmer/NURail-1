<span class="subject-name"><a href="http://eoh.ec.illinois.edu/" target="_blank">Engineering Open House</a></span><br><br>

Engineering Open House (EOH) is an annual event organized entirely by engineering students at the University of Illinois at Urbana-Champaign. It has been held each spring semester for over 85 years.  One way RailTEC participates in EOH is by working with major railroads to set up various exhibits and displays of railroad technology. With over 250 unique exhibits and attractions, EOH draws estimated crowds of 20,000 people including, students, teachers, parents and families, from all over Illinois and beyond.
