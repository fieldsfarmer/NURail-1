                             <h4>Michigan Tech</h4>
 
<span class="subject-name"><a href="http://www.rail.mtu.edu/summer_youth.html" target="_blank">Rail and Intermodal Transportation</a></span><br><br>
 
A week-long event split between the campuses of Michigan Tech and the University of Wisconsin Superior. Explore the wide world of rail and intermodal transportation through exciting field trip visits, and hands on learning opportunities while staying on campus with others in your group.<br><br>
Why are trains called the "green transportation alternative"? How fast can high-speed passenger trains travel? How do containers find their way from China to the U.S.? Find out the answers to these questions and more. Enjoy technical and hands-on tours of rail and intermodal facilities in Ishpeming/Marquette and Duluth/Superior where you can pose your questions directly to the experts.<br><br>
