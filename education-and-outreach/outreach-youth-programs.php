<span class="subject-name">Boy Scouts of America Railroading Merit Badge</span><br><br>
At a two-Saturday event called <a href="https://sites.google.com/site/illinoisnovaree/home" target="blank">Illinois Novaree</a>, University of Illinois RailTEC students and alumni teach the Boy Scouts of America Railroading merit badge at a STEM (science, technology, engineering, or mathematics) merit badge clinic on campus. By earning this badge, Scouts can learn about the history of railroading, its place in modern society, careers in railroading, and hobbies related to railroading. The material also includes an Operation Lifesaver presentation along with other information about how to safely operate around the tracks. 
