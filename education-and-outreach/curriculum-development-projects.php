                            <h1> Curriculum Development Projects</h1>
                             NURail’s vision to expand rail education and workforce development takes on several forms:<br><br>
1.	The NURail Center will focus on the development of new courses, adding substantial breadth and depth to the existing courses in the U.S. <br>
2.	New short courses will be developed and taught, focusing on topics that are relevant to U.S. rail research and operational needs such as shared corridor operation, freight rail safety, urban transport, and high speed rail engineering<br>
3.	Expanded outreach efforts will be developed to attract grade and high school students to the field and meet the growing demand for new employees.<br><br>
All of these efforts will facilitate the training and development of a rail transportation and engineering workforce that is prepared to design, deploy, operate, and maintain the complex rail and multi-modal transportation systems of the future. 
Key focus areas within NURail’s educational program are high speed rail engineering, shared corridor infrastructure design and operation, and the continued challenges associated with increasing heavy-axle-load freight train operation and the steady increase in gross railcar loads. These education areas also map to NURail’s research thrusts, which belong at the center of any rail teaching and research program due to the unique aspects of U.S. rail operations.<br><br>
